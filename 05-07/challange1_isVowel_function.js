/*isVowel() function

Write a function that takes a character
(i.e. a string of length 1) and returns
true if it is a vowel, false otherwise.*/

function isVowel(letra){
	if(letra === 'a' || letra ==='e' || letra === 'i' || letra === 'o' || letra === 'u'){
		return true;
	} else {
		return false;
	}
}